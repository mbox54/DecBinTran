#include "stdafx.h"


typedef unsigned char BYTE;

int main()
{
	BYTE v_Value[40] = {
		0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 
		0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78,
		0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78,
		0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78,
		0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78, 0x78
	};

	BYTE k = 0;
	BYTE bAct = 1;
	while (bAct)
	{
		if (v_Value[k] > 0)
		{
			// [ITERATE]

			v_Value[k] --;

		}
		else
		{
			if (k < 31)
			{
				// [NEXT BYTE]

				k++;
			}
			else
			{
				// [END]

				bAct = 0;
			}
		}

	}

    return 0;
}

